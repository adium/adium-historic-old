- (void)testHyperlink:(NSString *)linkString {
	SHHyperlinkScanner	*scanner = [[SHHyperlinkScanner alloc] initWithStrictChecking:NO];
	
	STAssertTrue([scanner isStringValidURL:linkString], @"%@ is not a valid URL", linkString);
}

